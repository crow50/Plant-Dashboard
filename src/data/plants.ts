export function getPlantById(id: string): PlantDbEntry | undefined {
  return PLANT_DATABASE.find(p => p.id === id);
}

export function searchPlants(query: string): PlantDbEntry[] {
  const q = query.toLowerCase();
  return PLANT_DATABASE.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.scientificName.toLowerCase().includes(q) ||
    p.commonNames.some(n => n.toLowerCase().includes(q))
  );
}
